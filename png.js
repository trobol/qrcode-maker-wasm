const { IDAT, IEND, IHDR, PLTE } = require('./Chunk');
const BinaryBuffer = require('./BinaryBuffer');

const qrcode = require('./qrcode');

function PNG(width, height, bitmap) {
	this.width = width;
	this.height = height;
	let scanlines = new Array(height);
	for (let a = 0; a < height; a++) {
		scanlines[a] = new Array(width + 8);
		for (let b = 0; b < 8; b++) {
			scanlines[a][b] = 0;
		}
		for (let c = 0; c < width; c++) {
			scanlines[a][c + 8] = bitmap[a * width + c];
		}
	}
	this.chunks = [
		new IHDR(width, height, 1, 3),
		new PLTE([0xff, 0xff, 0xff, 0x00, 0x00, 0x00]),
		new IDAT(scanlines),
		new IEND()
	];
}

PNG.prototype.toBuffer = function () {
	const buffer = new BinaryBuffer();
	buffer.write([
		0x89, 0x50, 0x4e, 0x47, //png file header
		0x0d, 0x0a, 0x1a, 0x0a
	])
	for (let chunk of this.chunks) {
		chunk.toBuffer(buffer);
	}
	return buffer;
}


